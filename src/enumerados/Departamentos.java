package enumerados;

/**
 * Enum que representa los diferentes departamentos en los que pueden estar
 * asignados los trabajadores. Cada valor del enum corresponde a un departamento
 * específico dentro de la organización.
 * 
 * <p>
 * Este enum facilita la asignación y gestión de los empleados dentro de
 * distintos departamentos, permitiendo una estructura organizada y clara de las
 * distintas áreas de trabajo.
 * </p>
 * 
 * @author [Miguel, Sergio, Daniel y Crisveling]
 * @version 1.0
 * @since 2025
 */
public enum Departamentos {

	// Definición de los departamentos disponibles en la organización
	Limpieza_eliminados, // Departamento encargado de la limpieza de los eliminados.
	Preparación_juegos, // Departamento encargado de la preparación de los juegos.
	Limpieza_mantenimiento, // Departamento responsable del mantenimiento y limpieza general.
	Distribución_alimentos; // Departamento encargado de la distribución de alimentos.

}
